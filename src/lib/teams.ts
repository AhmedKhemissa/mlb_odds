export const getTeamById = (id: string): MLBTeam | undefined => {
  return MLB_TEAMS.find(team => team.id === id);
};

export const getTeamByName = (name: string): MLBTeam | undefined => {
  return MLB_TEAMS.find(team => 
    team.name.toLowerCase().includes(name.toLowerCase()) ||
    team.shortName.toLowerCase().includes(name.toLowerCase()) ||
    team.city.toLowerCase().includes(name.toLowerCase())
  );
};
